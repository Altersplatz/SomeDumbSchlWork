polygon = int(input("Enter the number of sides the polygon has : "))

if polygon == 3:
    print("This polygon is a triangle")
elif polygon == 4:
    print("This polygon is a quadrilateral")
elif polygon == 5:
    print("This polygon is a pentagon")
elif polygon == 6:
    print("This polygon is a hexagon")
elif polygon == 7:
    print("This polygon is a septagon")
elif polygon == 8:
    print("This polygon is a octagon")
elif polygon == 9:
    print("This polygon is a nonagon")
elif polygon == 10:
    print("This polygon is a decagon")
elif polygon == 1 or 2 or 0:
    print("Not Applicable!")